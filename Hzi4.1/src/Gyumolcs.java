public class Gyumolcs {
    private String fajta;
    private String iz;

    public Gyumolcs(String fajta, String iz) {
        this.fajta = fajta;
        this.iz = iz;
    }

    public String milyenFajtaju() {
        return this.fajta;
    }

    public String milyenIzu() {
        return this.iz;
    }
    
    
}
