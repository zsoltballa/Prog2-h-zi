public class Alap {
    
    private String hozzavalo;
    private int ar;

    public Alap(String hozzavalo, int ar) {
        this.hozzavalo = hozzavalo;
        this.ar = ar;
    }

    public String getHozzavalo() {
        return hozzavalo;
    }

    public void setHozzavalo(String hozzavalo) {
        this.hozzavalo = hozzavalo;
    }

    public int getAr() {
        return ar;
    }

    public void setAr(int ar) {
        this.ar = ar;
    }
    
    
    
}
